/** If Else Statement  */
var a = 30;
if(a < 100) {
    console.log("a is less than 100");
} else {
    console.log("a is more than 100");
}

/** If Else If Statement */
var b = 50;
if(b == 30) {
    console.log("b is equal to 30");   
} else if(b < 60) {
    console.log("b is less than 60");
} else {
    console.log("b is more than 60");
}